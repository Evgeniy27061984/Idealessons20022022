package CoursePackages.Lesson1.HomeWork;

public class Lesson2 {
    public static void main(String[] args) {
        byte a = 12; // десятичная система исчисления
        byte a2 = 0B1100; // двоичная
        byte a3 = 014; // восьмеричная
        byte a4 = 0xC; // шестнадцатиричная

        System.out.println(a);     // 60
        System.out.println(a2);     // 48
        System.out.println(a3);     // 60
        System.out.println(a4);     // 60

        short s = 1300; // десятичная система исчисления
        short s2 = 0B010100010100; // двоичная
        short s3 = 02424; // восьмеричная
        short s4 = 0x514; // шестнадцатиричная

        System.out.println(s);     // 1300
        System.out.println(s2);     // 1300
        System.out.println(s3);     // 1300
        System.out.println(s4);     // 1300

        int i = 0; // десятичная система исчисления
        int i2 = 0B0000; // двоичная
        int i3 = 00; // восьмеричная
        int i4 = 0x0; // шестнадцатиричная

        System.out.println(i);     // 1300
        System.out.println(i2);     // 1300
        System.out.println(i3);     // 1300
        System.out.println(i4);     // 1300

        long l = 123456789; // десятичная система исчисления
        long l2 = 0B0111010110111100110100010101; // двоичная
        long l3 = 0726746425; // восьмеричная
        long l4 = 0x75BCD15; // шестнадцатиричная

        System.out.println(l);     // 123456789
        System.out.println(l2);     // 123456789
        System.out.println(l3);     // 123456789
        System.out.println(l4);     // 123456789

        float f = 2.3f;
        float f2 = 2.3f;
        double d = 2.3d;
        double d2 = 2.3;
        boolean t = false;
        boolean  t2 = true;

        System.out.println(f);
        System.out.println(f2);
        System.out.println(d);
        System.out.println(d2);
        System.out.println(t);
        System.out.println(t2);

        char c = 'A';
        char c2 = 300;
        char c3 = '\u0556';

        System.out.println(c);
        System.out.println(c2);
        System.out.println(c3);

    }

}
